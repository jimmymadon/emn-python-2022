print("Arithmetic Operators:")
print("2+2:", 2+2)
print("2-2:", 2-2)
print("2*2:", 2*2)
print("2/2:", 2/2)
print("10%3 (Modulus/Remainder):", 10%3)
print("10//3 (Floor Division/Quotient):", 10//3)
print("2**3:", 2**3)
print("2**4:", 2**4)
print()

print("Comparison Operators:")
print("10 > 5 :", 10 > 5) # 10 greater than 5 is True
print("10 < 5 :", 10 < 5) # 10 lesser than 5 is False
print("10 >= 5 :", 10 >= 5) # 10 greater than or equal to 5 is True
print("5 <= 5 :", 5 <= 5) # 5 lesser than or equal to 5 is True
print("10 == 5 :", 10 == 5) # 10 is equal (equivalent) to 5 is False
print("10 != 5 :", 10 != 5) # 10 is not equal to 5 is True

print("Logical Operators:")
print("True and True :", True and True)
print("True and False :", True and False)
print("False and True :", False and True)
print("False and False :", False and False)
print()

print("True or True :", True or True)
print("True or False :", True or False)
print("False or True :", False or True)
print("False or False :", False or False)
print()

print("not True :", not True)
print("not False :", not False)
print()
